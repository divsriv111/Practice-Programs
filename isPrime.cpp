#include <iostream>
#include <bits/stdc++.h>
using namespace std;

bool IsPrime(long n)
{
    //much efficient
    if (n == 1)
        return false;

    if (n == 2 || n == 3)
        return true;

    if (n % 2 == 0 || n % 3 == 0)
        return false;

    for (int i = 5; i <= sqrt(n); i += 6)
    {
        if (n % i == 0 || n % (i + 2) == 0)
            return false;
    }
    return true;
}

int main()
{
    //region READ FROM FILE
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);
    //end region

    long a;
    cin >> a;
    cout << IsPrime(a);

    return 0;
}